Bem-vindo ao **XLata**, o sistema mais completo para gestão de depósitos de reciclagem e ferro velhos do Brasil. Este guia vai te ajudar a configurar tudo em poucos minutos.

## Por que escolher o XLata?

O XLata foi desenvolvido especialmente para donos de depósitos de reciclagem que querem:

- **Abandonar papel e caneta** de vez
- **Ter controle total** sobre compras e vendas
- **Aumentar a lucratividade** com relatórios precisos
- **Economizar tempo** na balança e no caixa

## Primeiros Passos

### 1. Acesse sua conta

Após criar sua conta em [xlata.site/register](/register), você receberá acesso imediato ao sistema. Use seu email e senha para fazer login.

### 2. Configure seus materiais

O primeiro passo é cadastrar os materiais que você compra e vende:

1. Acesse o menu **Materiais**
2. Clique em **Adicionar Material**
3. Informe o nome, preço de compra e preço de venda
4. Salve e repita para cada material

**Dica:** Comece pelos materiais mais comuns como ferro, alumínio, cobre, papelão e plástico.

### 3. Abra o caixa

Antes de iniciar as operações do dia:

1. Vá para o **PDV** (Ponto de Venda)
2. Clique em **Abrir Caixa**
3. Informe o valor inicial em dinheiro
4. Confirme a abertura

### 4. Registre sua primeira compra

Com o caixa aberto, você pode registrar compras:

1. No PDV, selecione o tipo de operação (Compra)
2. Escolha o material
3. Informe o peso
4. O sistema calcula automaticamente o valor
5. Confirme e imprima o recibo

