  '# 10 Erros Comuns na Gestão de Ferro Velho

Muitos donos de ferro velho cometem erros que prejudicam seus lucros. Conheça os mais comuns e aprenda a evitá-los.

## 1. Não Controlar o Caixa

**O erro:** Misturar dinheiro pessoal com o do negócio, não registrar todas as operações.

**A solução:** Use um sistema de controle de caixa. Abra e feche o caixa diariamente. Registre TODAS as movimentações.

## 2. Preços Desatualizados

**O erro:** Comprar e vender com preços defasados, perdendo margem ou clientes.

**A solução:** Acompanhe cotações semanalmente. Ajuste preços rapidamente. Use sistema que facilite atualização.

## 3. Não Conhecer Suas Margens

**O erro:** Não saber quanto está ganhando em cada material.

**A solução:** Calcule o custo de aquisição e compare com o preço de venda. Considere despesas operacionais.

## 4. Balança Descalibrada

**O erro:** Balança com erro de leitura, causando prejuízo em todas as pesagens.

**A solução:** Calibre a balança regularmente. Faça verificações com peso padrão. Mantenha manutenção em dia.

## 5. Falta de Organização

**O erro:** Materiais misturados, dificultando separação e vendas.

**A solução:** Organize o espaço por tipo de material. Use identificação visual. Mantenha corredores livres.

## 6. Vender para Poucos Compradores

**O erro:** Depender de um único comprador, ficando refém de seus preços.

**A solução:** Mantenha relacionamento com múltiplos compradores. Faça cotações antes de vender. Negocie melhores condições.

## 7. Não Fidelizar Fornecedores

**O erro:** Perder bons fornecedores para concorrentes.

**A solução:** Ofereça preços justos. Atenda com rapidez e respeito. Pague em dia. Mantenha relacionamento.

## 8. Ignorar Despesas Fixas

**O erro:** Não considerar aluguel, luz, funcionários no cálculo de lucro.

**A solução:** Liste todas as despesas fixas. Divida pelo volume de operações. Inclua no cálculo de margem.

## 9. Não Usar Tecnologia

**O erro:** Insistir em papel, caneta e calculadora em pleno século XXI.

**A solução:** Adote um sistema de gestão. Automatize processos. Elimine erros manuais.

## 10. Falta de Planejamento

**O erro:** Operar sem metas, sem planejamento, sem visão de futuro.

**A solução:** Defina metas mensais. Acompanhe indicadores. Planeje crescimento.

